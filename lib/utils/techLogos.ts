/**
 * Get technology logo URL
 * Returns custom iconUrl if provided, otherwise tries to get from CDN
 */
export function getTechLogoUrl(techName: string, customIconUrl?: string): string {
  if (customIconUrl) {
    return customIconUrl;
  }

  // Try exact match first
  if (TECH_LOGO_CDN[techName]) {
    return TECH_LOGO_CDN[techName];
  }

  // Try case-insensitive match
  const normalizedName = techName.toLowerCase();
  for (const [key, value] of Object.entries(TECH_LOGO_CDN)) {
    if (key.toLowerCase() === normalizedName) {
      return value;
    }
  }

  // Return placeholder or empty
  return '';
}

/**
 * Check if a technology has an official logo available
 */
export function hasTechLogo(techName: string): boolean {
  const normalizedName = techName.toLowerCase();
  return Object.keys(TECH_LOGO_CDN).some(key => key.toLowerCase() === normalizedName);
}

/**
 * Get all available technology names with logos
 */
export function getAvailableTechNames(): string[] {
  return Object.keys(TECH_LOGO_CDN).sort();
}
